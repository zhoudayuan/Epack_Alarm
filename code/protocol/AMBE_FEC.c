/**
 * @brief   交织函数
 * @param [in]     SrcBuf
 * @param [out]   DstBuf 
 * @author  张超
 * @since   fec1.0
 * @bug
 */
static void AMBE_Interleave(UINT16 *SrcBuf, UINT16 *DstBuf)
{
	int i;
	UINT16 tmp[72]={0};
	for (i = 0; i < 18; i++)
	{
		tmp[i * 4 + 0] = SrcBuf[0+i];
		tmp[i * 4 + 1] = SrcBuf[18+i];
		tmp[i * 4 + 2] = SrcBuf[36+i];
		tmp[i * 4 + 3] = SrcBuf[54+i];
	}
	memcpy((UINT8*)DstBuf,(UINT8*)tmp,sizeof(tmp));
}


/**
 * @brief   解交织函数
 * @param [in]     SrcBuf
 * @param [out]   DstBuf 
 * @author  张超
 * @since   fec1.0
 * @bug
 */
static void AMBE_UnInterleave(UINT16 *SrcBuf, UINT16 *DstBuf)
{
	int i;
	UINT16 tmp[72] = {0};
	for (i = 0; i < 18; i++)
	{
		tmp[0 + i] = SrcBuf[i * 4 + 0];
		tmp[18 + i] = SrcBuf[i * 4 + 1];
		tmp[36 + i] = SrcBuf[i * 4 + 2];
		tmp[54 + i] = SrcBuf[i * 4 + 3];
	}
	memcpy((UINT8*)DstBuf,(UINT8*)tmp,sizeof(tmp));
}




